CHECKS: list[tuple[str, bool]] = []


def check(name: str, ok: bool, detail: str = "") -> None:
    CHECKS.append((name, bool(ok)))
    line = f"[{'PASS' if ok else 'FAIL'}] {name}"
    if detail:
        line += f"   ({detail})"
    print(line)


# ------------------------------------------------------------- Cartan data
def cartan_A(n):
    C = [[0] * n for _ in range(n)]
    for i in range(n):
        C[i][i] = 2
        if i + 1 < n:
            C[i][i + 1] = C[i + 1][i] = -1
    return C


def cartan_B(n):                     # so(2n+1): alpha_n short, C_{n-1,n} = -2
    C = cartan_A(n)
    C[n - 2][n - 1] = -2
    C[n - 1][n - 2] = -1
    return C


def cartan_C(n):                     # sp(2n): transpose of B_n
    C = cartan_A(n)
    C[n - 2][n - 1] = -1
    C[n - 1][n - 2] = -2
    return C


def cartan_D(n):                     # so(2n): fork at node n-3 (0-indexed)
    C = [[0] * n for _ in range(n)]
    for i in range(n):
        C[i][i] = 2
    for i in range(n - 2):
        if i + 1 < n - 1:
            C[i][i + 1] = C[i + 1][i] = -1
    C[n - 3][n - 1] = C[n - 1][n - 3] = -1
    return C


def cartan_tree(arms):
    """Simply-laced tree: paths of vertex-lengths p,q,r glued at one end.
    T(3,3,2)=E6, T(4,3,2)=E7, T(5,3,2)=E8, T(2,2,2)=D4."""
    nodes = 1 + sum(a - 1 for a in arms)
    C = [[0] * nodes for _ in range(nodes)]
    for i in range(nodes):
        C[i][i] = 2
    idx = 1
    for a in arms:
        prev = 0
        for _ in range(a - 1):
            C[prev][idx] = C[idx][prev] = -1
            prev = idx
            idx += 1
    return C


def cartan_F4():
    C = cartan_A(4)
    C[1][2] = -2                     # middle double bond
    C[2][1] = -1
    return C


# --------------------------------------------------- root system machinery
def positive_roots(C):
    """Positive roots in simple-root coordinates, generated by height."""
    n = len(C)
    roots = {tuple(1 if j == i else 0 for j in range(n)) for i in range(n)}
    level = list(roots)
    allroots = set(roots)
    while level:
        nxt = set()
        for beta in level:
            pairing = [sum(beta[j] * C[j][i] for j in range(n)) for i in range(n)]
            for i in range(n):
                p = 0
                cur = list(beta)
                while True:
                    cur[i] -= 1
                    if cur[i] < 0 or tuple(cur) not in allroots:
                        break
                    p += 1
                if p - pairing[i] >= 1:
                    cand = list(beta)
                    cand[i] += 1
                    cand = tuple(cand)
                    if cand not in allroots:
                        nxt.add(cand)
        allroots |= nxt
        level = list(nxt)
    return sorted(allroots)


def symmetrizers(C):
    """d_i with d_j C_ij = d_i C_ji (connected diagram assumed)."""
    n = len(C)
    d = [None] * n
    d[0] = Fr(1)
    todo = [0]
    while todo:
        i = todo.pop()
        for j in range(n):
            if i != j and C[i][j] != 0 and d[j] is None:
                d[j] = d[i] * Fr(C[j][i], C[i][j])
                todo.append(j)
    return d


def weyl_dim(posroots, d, labels):
    dim = Fr(1)
    for c in posroots:
        num = sum(c[j] * d[j] * (labels[j] + 1) for j in range(len(labels)))
        den = sum(c[j] * d[j] for j in range(len(labels)))
        dim *= Fr(num, den)
    return dim


def w0_involution(C, n_pos):
    """rho-descent word for w0; returns (word_length, sigma) where sigma is
    the permutation with -w0(omega_m) = omega_sigma(m), or None if -w0 != perm."""
    n = len(C)
    v = [1] * n
    word = []
    while any(x > 0 for x in v):
        i = next(k for k in range(n) if v[k] > 0)
        ai = v[i]
        v = [v[j] - ai * C[i][j] for j in range(n)]
        word.append(i)

    def apply_word(u):
        u = list(u)
        for i in word:
            ui = u[i]
            u = [u[j] - ui * C[i][j] for j in range(n)]
        return u

    sigma = []
    for m in range(n):
        img = [-x for x in apply_word([1 if k == m else 0 for k in range(n)])]
        if sorted(img) != [0] * (n - 1) + [1]:
            return len(word), None
        sigma.append(img.index(1))
    return len(word), tuple(sigma)


def enumerate_reps(rank, dimfun, cap):
    out = []

    def rec(prefix):
        if len(prefix) == rank:
            if any(prefix):
                dd = dimfun(prefix)
                if dd <= cap:
                    out.append((tuple(prefix), int(dd)))
            return
        k = 0
        while True:
            cand = prefix + [k]
            probe = tuple(cand + [0] * (rank - len(cand)))
            if any(probe) and dimfun(probe) > cap:
                break
            rec(cand)
            k += 1

    rec([])
    return out


