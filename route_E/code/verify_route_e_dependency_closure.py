print("== Section 6: CI-3 removal -- the chiral 16 of Spin(10), all 45^3 triples ==")

NMODE = 5
DIM = 1 << NMODE
b = []
for k in range(NMODE):
    M = np.zeros((DIM, DIM), dtype=complex)
    for s in range(DIM):
        if s & (1 << k):
            sign = (-1) ** bin(s & ((1 << k) - 1)).count("1")
            M[s ^ (1 << k), s] = sign
    b.append(M)
bd = [M.conj().T for M in b]

Gam = []
for k in range(NMODE):
    Gam.append(b[k] + bd[k])
    Gam.append(-1j * (b[k] - bd[k]))

ok_cliff = all(
    np.allclose(Gam[a] @ Gam[c] + Gam[c] @ Gam[a], 2 * np.eye(DIM) * (a == c))
    for a in range(10) for c in range(a, 10)
)
