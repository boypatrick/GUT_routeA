def threshold_rows() -> List[Dict[str, Any]]:
    branches = [
        ("singlet_auxiliaries", B_SINGLET, "no threshold"),
        ("degenerate_10G_5pair", B_FIVE_PAIR, "universal only"),
        ("triplet_only_bridge", B_TRIPLET_PAIR, "non-universal"),
        ("complete_54_pair", B_54_PAIR, "universal only"),
        ("complete_210_pair", B_210_PAIR, "universal only"),
        ("Wrel_54_source_block", B_REL_SOURCE_54_BLOCK, "universal only"),
        ("XiN_incomplete_fallback", B_XIN_NORMAL_PAIR, "non-universal"),
    ]
    rows: List[Dict[str, Any]] = []
    for name, beta, status in branches:
        for kappa in KAPPA_GRID:
            d = delta(beta, kappa)
            rows.append(
                {
                    "branch": name,
                    "kappa_M_over_MG": kappa,
                    "threshold_status": status,
                    "delta": d.tolist(),
                    "projected_delta_l2": projected_l2(d),
                    "universal_delta": float(np.mean(d)),
                }
            )
    return rows


def window_rows() -> List[Dict[str, Any]]:
    rows: List[Dict[str, Any]] = []
    for name, beta in [
        ("triplet_only_bridge", B_TRIPLET_PAIR),
        ("XiN_incomplete_fallback", B_XIN_NORMAL_PAIR),
    ]:
        for tol in TOLS:
            row = clean_window(beta, tol)
            row["branch"] = name
            rows.append(row)
    return rows


def uv_rows() -> List[Dict[str, Any]]:
    # Local UV audit used alpha_G^{-1}=41.363... at the R=200 corrected branch.
    alpha_ref = 41.36308203884868
    scenarios = [
        ("baseline_minimal_yukawa_higgs_matter", 70.0, "three 16s plus 10_H,120_H,126bar_H"),
        ("baseline_plus_sterile_10G", 71.0, "minimal Schur completion adds T(10)=1"),
        ("documented_complete_drive_pairs", 184.0, "three 54 link/driver pairs plus one 210 pair"),
        ("documented_drive_pairs_plus_10G", 185.0, "drive-pair branch plus sterile 10'_G"),
        ("Wrel_source_blocks", 300.0, "eleven-field 54 relative-source block plus 210 source block"),
    ]
    rows = []
    for name, sum_t, interp in scenarios:
        b10 = sum_t - 24.0
        lp = landau_ratio(alpha_ref, sum_t)
        rows.append(
            {
                "scenario": name,
                "sum_T": sum_t,
                "b10": b10,
                "alphaG_inv_ref": alpha_ref,
                "landau_ratio_Lambda_over_MG": lp,
                "reaches_R50": bool(lp > 50.0),
                "reaches_R200": bool(lp > 200.0),
                "interpretation": interp,
            }
        )
    return rows


def write_csv(path: Path, rows: List[Dict[str, Any]]) -> None:
    fields = sorted({key for row in rows for key in row.keys()})
    with path.open("w", newline="", encoding="utf-8") as handle:
        writer = csv.DictWriter(handle, fieldnames=fields)
        writer.writeheader()
        for row in rows:
            out = dict(row)
            for key, value in list(out.items()):
                if isinstance(value, list):
                    out[key] = " ".join(f"{float(x):.12e}" for x in value)
            writer.writerow(out)


def make_report(payload: Dict[str, Any]) -> str:
    lines = [
        "# Schur auxiliary component-spectrum audit",
        "",
        "No web lookup was used.",
        "",
        "## Component classification",
        "",
        "| symbols | status | beta vector | projected beta l2 | verdict |",
        "|---|---|---:|---:|---|",
    ]
    for row in payload["components"]:
        lines.append(
            f"| `{row['symbol']}` | {row['classification']} | `{row['beta_vector']}` | "
            f"{row['projected_beta_l2']:.6e} | {row['verdict']} |"
        )
    lines.extend(
        [
            "",
            "## Clean windows for incomplete branches",
            "",
            "| branch | tolerance | kappa min | kappa max | |log kappa|max |",
            "|---|---:|---:|---:|---:|",
        ]
    )
    for row in payload["windows"]:
        lines.append(
            f"| `{row['branch']}` | {row['projected_tol']:.1e} | "
            f"{row['kappa_min']:.6f} | {row['kappa_max']:.6f} | "
            f"{row['max_abs_log_kappa']:.6e} |"
        )
    lines.extend(
        [
            "",
            "## UV perturbativity check",
            "",
            "| scenario | sum T | b10 | LP/MG | reaches R=50 | reaches R=200 |",
            "|---|---:|---:|---:|---:|---:|",
        ]
    )
    for row in payload["uv"]:
        lines.append(
            f"| `{row['scenario']}` | {row['sum_T']:.0f} | {row['b10']:.0f} | "
            f"{row['landau_ratio_Lambda_over_MG']:.3g} | {row['reaches_R50']} | {row['reaches_R200']} |"
        )
    lines.extend(
        [
            "",
            "## Verdict",
            "",
            payload["verdict"],
        ]
    )
    return "\n".join(lines) + "\n"


