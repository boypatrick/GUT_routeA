KAPPA_GRID = [0.3, 0.5, 0.9, 0.99, 1.0, 1.01, 1.1, 2.0, 3.0]
TOLS = [1.0e-2, 1.0e-3]


def projected_l2(vec: np.ndarray) -> float:
    return float(np.linalg.norm(base.PROJECTOR @ vec))


def delta(beta: np.ndarray, kappa: float) -> np.ndarray:
    return beta * math.log(1.0 / kappa) / (2.0 * math.pi)


def clean_window(beta: np.ndarray, tol: float) -> Dict[str, float]:
    p = projected_l2(beta)
    if p == 0.0:
        return {
            "projected_tol": tol,
            "projected_beta_l2": 0.0,
            "max_abs_log_kappa": math.inf,
            "kappa_min": 0.0,
            "kappa_max": math.inf,
        }
    width = 2.0 * math.pi * tol / p
    return {
        "projected_tol": tol,
        "projected_beta_l2": p,
        "max_abs_log_kappa": width,
        "kappa_min": math.exp(-width),
        "kappa_max": math.exp(width),
    }


def landau_ratio(alpha_inv: float, sum_t: float) -> float:
    b10 = sum_t - 24.0
    if b10 <= 0:
        return math.inf
    return math.exp(2.0 * math.pi * alpha_inv / b10)


