# N=1 chiral superfield beta vectors in MSSM normalization.
B_SINGLET = np.array([0.0, 0.0, 0.0], dtype=float)
B_TRIPLET_PAIR = np.array([2.0 / 5.0, 0.0, 1.0], dtype=float)
B_DOUBLET_PAIR = np.array([3.0 / 5.0, 1.0, 0.0], dtype=float)
B_FIVE_PAIR = B_TRIPLET_PAIR + B_DOUBLET_PAIR
B_54_PAIR = np.array([24.0, 24.0, 24.0], dtype=float)
B_210_PAIR = np.array([112.0, 112.0, 112.0], dtype=float)
